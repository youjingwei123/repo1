//form.serialize()；转json

function serializeToObject(jsonArray) {
    var o = {};
    $.each(jsonArray, function () {
        if (o[this.name] !== undefined) {
            if (!o[this.name].push) {
                o[this.name] = [o[this.name]];
            }
            o[this.name].push(this.value || '');
        } else {
            o[this.name] = this.value || '';
        }
    });
    return o;
}